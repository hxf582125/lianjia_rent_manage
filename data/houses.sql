CREATE TABLE `houses` (
  `id` int NOT NULL AUTO_INCREMENT,
  `biz_key` varchar(255) NOT NULL DEFAULT '' COMMENT '房源唯一key',
  `community` varchar(100) NOT NULL DEFAULT '' COMMENT '小区',
  `district` varchar(100) NOT NULL DEFAULT '' COMMENT '区域商圈',
  `layout` varchar(50) NOT NULL DEFAULT '' COMMENT '户型',
  `total_price` varchar(50) NOT NULL DEFAULT '' COMMENT '总价',
  `agent_unit_price` varchar(50) NOT NULL DEFAULT '' COMMENT '机构成交单价',
  `market_unit_price` varchar(50) NOT NULL DEFAULT '' COMMENT '挂牌均价',
  `agent_company` varchar(50) NOT NULL DEFAULT '' COMMENT '成交机构',
  `deal_date` varchar(50) NOT NULL DEFAULT '' COMMENT '成交时间',
  `area` varchar(50) NOT NULL DEFAULT '' COMMENT '面积',
  `floor` varchar(50) NOT NULL DEFAULT '' COMMENT '楼层',
  `listing_price` varchar(50) NOT NULL DEFAULT '' COMMENT '挂牌价',
  `transaction_cycle` varchar(50) NOT NULL DEFAULT '' COMMENT '成交周期',
  `orientation` varchar(50) NOT NULL DEFAULT '' COMMENT '朝向',
  `floor_detail` varchar(50) NOT NULL DEFAULT '' COMMENT '详细楼层',
  `building` varchar(50) NOT NULL DEFAULT '' COMMENT '楼栋号',
  `decoration` varchar(50) NOT NULL DEFAULT '' COMMENT '装修',
  `year_built` varchar(50) NOT NULL DEFAULT '' COMMENT '年代',
  `has_elevator` varchar(50) NOT NULL DEFAULT '' COMMENT '是否有电梯',
  `monthly_rent` varchar(50) NOT NULL DEFAULT '' COMMENT '月租金',
  `rent_yield` varchar(50) NOT NULL DEFAULT '' COMMENT '租金年回报率',
  `price_ref_percent` varchar(50) NOT NULL DEFAULT '' COMMENT '同户型价格参考单价比率',
  `price_change` varchar(50) NOT NULL DEFAULT '' COMMENT '均价上涨率',
  `rank_info` varchar(50) NOT NULL DEFAULT '' COMMENT '成交量排名',
  `bargain_amount` varchar(50) NOT NULL DEFAULT '' COMMENT '砍价金额',
  `bargain_percent` varchar(50) NOT NULL DEFAULT '' COMMENT '砍价比例',
  `project_source` varchar(50) NOT NULL DEFAULT '' COMMENT '项目来源',
  `payload_json` text NOT NULL COMMENT '完整数据',
  `saved_at` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `idx_houses_project_biz_key` (`project_source`,`biz_key`),
  KEY `ix_houses_saved_at` (`saved_at`),
  KEY `idx_houses_project_community_district` (`project_source`,`community`,`district`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci COMMENT='房源表';