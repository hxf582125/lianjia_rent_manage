CREATE TABLE `rent_house` (
  `id` int NOT NULL AUTO_INCREMENT,
  `biz_key` varchar(255) NOT NULL DEFAULT '' COMMENT '房源唯一key',
  `batch_id` varchar(255) NOT NULL DEFAULT '' COMMENT '批次id',
  `title` varchar(100) NOT NULL DEFAULT '' COMMENT '标题',
  `community` varchar(100) NOT NULL DEFAULT '' COMMENT '小区',
  `desc` varchar(100) NOT NULL DEFAULT '' COMMENT '房源描述',
  `rent_type` varchar(50) NOT NULL DEFAULT '' COMMENT '出租方式',
  `area` varchar(50) NOT NULL DEFAULT '' COMMENT '面积',
  `orientation` varchar(50) NOT NULL DEFAULT '' COMMENT '朝向',
  `floor_detail` varchar(50) NOT NULL DEFAULT '' COMMENT '详细楼层',
  `loc_desc` varchar(50) NOT NULL DEFAULT '' COMMENT '位置描述',
  `total_price` varchar(50) NOT NULL DEFAULT '' COMMENT '租金',
  `tags` varchar(100) NOT NULL DEFAULT '' COMMENT '标签',
  `district` varchar(100) NOT NULL DEFAULT '' COMMENT '区域',
  `detail_district` varchar(100) NOT NULL DEFAULT '' COMMENT '详情区域',
  `bankuai` varchar(100) NOT NULL DEFAULT '' COMMENT '板块',
  `layout` varchar(50) NOT NULL DEFAULT '' COMMENT '户型',
  `bedroom` int NOT NULL DEFAULT '0' COMMENT '室',
  `livingroom` int NOT NULL DEFAULT '0' COMMENT '厅',
  `near_subway` int NOT NULL DEFAULT '0' COMMENT '近地铁',
  `check_in_time` varchar(50) NOT NULL DEFAULT '' COMMENT '入住时间',
  `pay_type` varchar(50) NOT NULL DEFAULT '' COMMENT '付款方式',
  `service_fee` varchar(50) NOT NULL DEFAULT '' COMMENT '服务费',
  `agent_fee` varchar(50) NOT NULL DEFAULT '' COMMENT '中介费',
  `deposit` varchar(50) NOT NULL DEFAULT '' COMMENT '押金',
  `build_date` varchar(50) NOT NULL DEFAULT '' COMMENT '建成时间',
  `build_type` varchar(50) NOT NULL DEFAULT '' COMMENT '建筑类型',
  `project_source` varchar(50) NOT NULL DEFAULT '' COMMENT '项目来源',
  `payload_json` text NOT NULL COMMENT '完整数据',
  `saved_at` datetime DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` datetime DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `idx_houses_project_biz_key` (`batch_id`,`project_source`,`biz_key`),
  KEY `ix_rent_house_bankuai` (`bankuai`),
  KEY `ix_rent_house_district` (`district`),
  KEY `ix_rent_house_saved_at` (`saved_at`),
  KEY `ix_rent_house_community` (`community`),
  KEY `idx_houses_project_community_district` (`project_source`,`community`,`district`,`bankuai`),
  KEY `ix_rent_house_detail_district` (`detail_district`),
  KEY `ix_rent_house_project_source` (`project_source`),
  KEY `ix_rent_house_batch_id` (`batch_id`)
) ENGINE=InnoDB AUTO_INCREMENT DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci COMMENT='租房房源表';